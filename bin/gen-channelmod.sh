#!/bin/bash
# =============================================================================
# gen-channelmod.sh — emit a channel model per UE for one cell
#
# Usage: gen-channelmod.sh <ues_per_cell> <out_file> [mode] [type]
#   mode: uniform  — every UE gets identical channel parameters (default)
#         gradient — path loss increases UE1 -> UEK (cell-centre to cell-edge)
#   type: AWGN (default) | TDL_A | TDL_B | TDL_C | EPA | EVA | ETU
#
# RFsim needs one model per UE: the gNB applies rfsimu_channel_enB0 on the
# uplink, and each UE applies its own rfsimu_channel_ue<N> on the downlink.
# With only ue0 defined, UE2 onward log "Model rfsimu_channel_ue1 not found"
# and run with no channel at all -- so this must scale with ues_per_cell.
#
# Values here are the initial state. They can be changed at runtime per model
# over telnet (channelmod modify <idx> ploss|noise_power_dB|riceanf|... ), and
# `channelmod show current` lists the live models with their indices.
# =============================================================================

set -e

UES="${1:-2}"
OUT="${2:-channelmod.conf}"
MODE="${3:-uniform}"
TYPE="${4:-AWGN}"

# Baseline (matches the validated single-node stack)
PLOSS_BASE=20
NOISE_GNB=-4
NOISE_UE=-2

# gradient: spread path loss from PLOSS_BASE to PLOSS_BASE + PLOSS_SPAN
PLOSS_SPAN=30

emit_model() {
    local name="$1" ploss="$2" noise="$3" trailing="$4"
    cat <<EOF
    {
      model_name     = "${name}"
      type           = "${TYPE}";
      ploss_dB       = ${ploss};
      noise_power_dB = ${noise};
      forgetfact     = 0;
      offset         = 0;
      ds_tdl         = 0;
    }${trailing}
EOF
}

{
  echo "# Generated by gen-channelmod.sh — ${UES} UEs, mode=${MODE}, type=${TYPE}"
  echo "# One model per UE. Tune at runtime via telnet:"
  echo "#   channelmod show current"
  echo "#   channelmod modify <idx> noise_power_dB 30"
  echo "channelmod = {"
  echo "  max_chan = $((UES + 2));"
  echo "  modellist = \"modellist_rfsimu_1\";"
  echo ""
  echo "  modellist_rfsimu_1 = ("
  emit_model "rfsimu_channel_enB0" "${PLOSS_BASE}" "${NOISE_GNB}" ","

  for u in $(seq 0 $((UES - 1))); do
      if [ "$MODE" = "gradient" ] && [ "$UES" -gt 1 ]; then
          # UE0 at cell centre, UE(K-1) at cell edge
          ploss=$((PLOSS_BASE + (u * PLOSS_SPAN) / (UES - 1)))
      else
          ploss=${PLOSS_BASE}
      fi
      if [ "$u" -eq $((UES - 1)) ]; then trail=""; else trail=","; fi
      emit_model "rfsimu_channel_ue${u}" "${ploss}" "${NOISE_UE}" "${trail}"
  done

  echo "  );"
  echo "};"
} > "$OUT"

echo "[CHANMOD] wrote ${OUT}: 1 gNB model + ${UES} UE models (${MODE}, ${TYPE})"
grep -c 'model_name' "$OUT" | xargs echo "[CHANMOD] total models:"
